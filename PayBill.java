    /**
    * Goes to Choose User if all the bills are empty and at least one is paid.
    * This is because the bills will also be empty if nothing has been ordered
    * yet so we only want it to do this is the bills are paid but there could be
    * only one bill in the transaction, hence at least one bill needs to be paid.
    */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if((menu.bill1.isEmpty() && menu.bill2.isEmpty() && menu.bill3.isEmpty())
                && (paid1 == true || paid2 == true || paid3 == true)){
            this.setVisible(false);
            new ChooseUser().setVisible(true);
        }else{
            this.setVisible(false);
            new Home().setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

