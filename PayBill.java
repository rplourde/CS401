    /**
    * If the bill is paid, resets the labels and text area and displays that the
    * bill is paid. If there is an empty bill that is not yet paid then labels
    * are set to invisible. If the bill is not empty and not paid, the bill is 
    * displayed.
    */
    public void updateBillOne(){
        if(menu.bill1.isEmpty() && paid1 == true){
            taxOne.setVisible(false);
            subtotalOne.setVisible(false);
            jLabel5.setVisible(true);
            jButton1.setVisible(true);
            jTextArea1.setText("");
            jLabel5.setText("Total: $" + df.format(menu.bill1.totalWithTax()));
            jButton1.setText("Bill Paid Thank You");
            menu.bill1 = new Bill();
        }else if(menu.bill1.isEmpty() && paid1 == false){
            taxOne.setVisible(false);
            subtotalOne.setVisible(false);
            jLabel5.setVisible(false);
            jButton1.setVisible(false);
        }else{
            taxOne.setVisible(true);
            subtotalOne.setVisible(true);
            jLabel5.setVisible(true);
            jButton1.setVisible(true);
            jTextArea1.setText(jTextArea1.getText() + menu.bill1.toString() + "\n");
            subtotalOne.setText("Subtotal: $" + df.format(menu.bill1.subTotal()));
            taxOne.setText("Tax: $" + df.format(menu.bill1.getTax()));
            jLabel5.setText("Total: $" + df.format(menu.bill1.totalWithTax()));
        }

        
    }
    
